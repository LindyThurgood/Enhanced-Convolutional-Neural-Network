import torch
import torch.nn as nn
from torch.utils.data import DataLoader, TensorDataset
from sklearn.metrics import accuracy_score, precision_score, f1_score
from sklearn.metrics import silhouette_score, davies_bouldin_score, calinski_harabasz_score
import numpy as np
import h5py

'''This script is designed to evaluate the performance of the student model trained using knowledge distillation from two teacher models on the LFW dataset. 
It loads the best student model, runs it on the full dataset, and computes various metrics including accuracy, f1, precision, and clustering metrics. 
It also saves the embeddings generated by the student model for potential use in downstream tasks.'''

# Model Definition from dual_teacher.py
class BetterStudent(nn.Module): 
    def __init__(self, num_classes):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(3, 32, 3, padding=1), nn.BatchNorm2d(32), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(32, 64, 3, padding=1), nn.BatchNorm2d(64), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(64, 128, 3, padding=1), nn.BatchNorm2d(128), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(128, 256, 3, padding=1), nn.BatchNorm2d(256), nn.ReLU(),
        )
        self.gap = nn.AdaptiveAvgPool2d(1)
        self.embedding = nn.Linear(256, 128)
        self.bn_emb = nn.BatchNorm1d(128)
        self.classifier = nn.Linear(128, num_classes)

    def forward(self, x, return_embeddings=False):
        x = self.features(x)
        x = self.gap(x).view(x.size(0), -1)
        emb = self.bn_emb(self.embedding(x))
        if return_embeddings: 
            return emb
        return self.classifier(emb)


# Utilize GPU and set paths
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model_path = "norm_lfw_full_student_T05.pth"
data_path = 'Full_lfw_preprocessed.pt'
h5_name = 'KD_Student_embeddings_T0.5.h5'


# Load Data & Initialize Model
print("Loading data...")
data = torch.load(data_path, map_location='cpu')
X = data['images']
Y = data['labels']

if isinstance(X, np.ndarray):
    X = torch.from_numpy(X).float().transpose(3, 1) 
    Y = torch.from_numpy(Y).long()
else:
    X = X.float().permute(0, 3, 1, 2) if X.shape[1] != 3 else X.float()
    Y = Y.long()

num_classes = 5749
#num_classes = len(torch.unique(Y))
dataset = TensorDataset(X, Y)
loader = DataLoader(dataset, batch_size=64, shuffle=False)

print(f"Loading Student Model from {model_path}...")
model = BetterStudent(num_classes=num_classes).to(device)
model.load_state_dict(torch.load(model_path, map_location=device))
model.eval()


# Metric Extraction
all_preds_list, all_trues_list, all_embeddings_list = [], [], []

print("Running evaluation")
with torch.no_grad():
    for x, y in loader:
        x = x.to(device)
        logits = model(x)
        preds = torch.argmax(logits, dim=1)
        embeddings = model(x, return_embeddings=True)
        all_preds_list.append(preds.cpu().numpy())
        all_trues_list.append(y.cpu().numpy())
        all_embeddings_list.append(embeddings.cpu().numpy())


all_embeddings = np.vstack(all_embeddings_list)
all_trues = np.concatenate(all_trues_list)
all_preds = np.concatenate(all_preds_list)


# Display results
print("\n" + "="*30)
print("Evaluation Results")
print("="*30)

acc = accuracy_score(all_trues, all_preds)
f1 = f1_score(all_trues, all_preds, average='weighted', zero_division=0)
prec = precision_score(all_trues, all_preds, average='weighted', zero_division=0)

print(f"Accuracy:  {acc:.4f}")
print(f"Precision: {prec:.4f}")
print(f"F1 Score:  {f1:.4f}")
print("-" * 30)

# Silhouette (Subsample for speed if dataset is large)
if len(all_embeddings) > 5000:
    idx = np.random.choice(len(all_embeddings), 5000, replace=False)
    sil = silhouette_score(all_embeddings[idx], all_trues[idx])
else:
    sil = silhouette_score(all_embeddings, all_trues)

ch_score = calinski_harabasz_score(all_embeddings, all_trues)
db_index = davies_bouldin_score(all_embeddings, all_trues)

print(f"Silhouette Score:      {sil:.4f}")
print(f"Calinski-Harabasz:     {ch_score:.4f}")
print(f"Davies-Bouldin Index:  {db_index:.4f}")
print("="*30)


# Save embeddings for student model (typically part of the CNN training script, but added here for simplicity)
print(f"Saving embeddings to {h5_name}...")
with h5py.File(h5_name, 'w') as hf: 
    hf.create_dataset('embeddings', data=all_embeddings, compression="gzip")
    hf.create_dataset('labels', data=all_trues, compression="gzip")
