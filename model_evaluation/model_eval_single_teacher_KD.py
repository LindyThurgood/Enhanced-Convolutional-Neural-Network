import torch
import torch.nn as nn
from torch.utils.data import DataLoader, TensorDataset
from sklearn.metrics import accuracy_score, precision_score, f1_score
from sklearn.metrics import silhouette_score, davies_bouldin_score, calinski_harabasz_score
import numpy as np
import h5py
import matplotlib.pyplot as plt
from sklearn.manifold import TSNE

'''This script is designed to evaluate the performance of the student model trained using knowledge distillation from a single teacher model. 
It loads the best student model, runs it on the full dataset, and computes various metrics including accuracy, f1, precision, and clustering metrics. 
It also saves the embeddings generated by the student model for later use.'''

# Data Loading and Configuration
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model_path = "kd_ed_Olivetti_T2A08.pth" 
data_path = 'olivetti_denoised_zscore_aug2.pt'
h5_name = 'kd_ed_Olivetti_T2A08_embeddings.h5'

print("Loading data...")
data = torch.load(data_path, map_location='cpu', weights_only=True)
X, Y = data['images'].float(), data['labels']
if isinstance(Y, np.ndarray):
    Y = torch.from_numpy(np.squeeze(Y)).long()
else:
    Y = Y.squeeze().long()

num_classes = len(torch.unique(Y))
loader = DataLoader(TensorDataset(X, Y), batch_size=64, shuffle=False)

# Model Definition
class StudentModel(nn.Module): 
    def __init__(self, num_classes):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(1, 32, 3, padding=1), nn.BatchNorm2d(32), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(32, 64, 3, padding=1), nn.BatchNorm2d(64), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(64, 128, 3, padding=1), nn.BatchNorm2d(128), nn.ReLU(), nn.MaxPool2d(2),
            nn.Conv2d(128, 256, 3, padding=1), nn.BatchNorm2d(256), nn.ReLU()
        )
        self.gap = nn.AdaptiveAvgPool2d(1)
        self.embedding = nn.Linear(256, 128)
        self.bn_emb = nn.BatchNorm1d(128)
        self.classifier = nn.Linear(128, num_classes)

    def forward(self, x, return_embeddings=False):
        x = self.features(x)
        x = self.gap(x).view(x.size(0), -1)
        emb = self.bn_emb(self.embedding(x))
        if return_embeddings:
            return emb
        return self.classifier(emb)



# Load model 
model = StudentModel(num_classes=num_classes).to(device)
model.load_state_dict(torch.load(model_path, map_location=device, weights_only=True))
model.eval()

# Evaluation loop
all_preds, all_trues, all_embeddings = [], [], []

print("Running evaluation...")
with torch.no_grad():
    for x, y in loader:
        x = x.to(device)
        embeddings = model(x, return_embeddings=True)
        logits = model.classifier(embeddings)
        preds = torch.argmax(logits, dim=1)
        
        all_preds.append(preds.cpu().numpy())
        all_trues.append(y.cpu().numpy())
        all_embeddings.append(embeddings.cpu().numpy())

all_embeddings = np.vstack(all_embeddings)
all_trues = np.concatenate(all_trues)
all_preds = np.concatenate(all_preds)

# Metrics Calculation
acc = accuracy_score(all_trues, all_preds)
# Weighted F1 accounts for class imbalance
f1 = f1_score(all_trues, all_preds, average='weighted', zero_division=0)
prec = precision_score(all_trues, all_preds, average='weighted', zero_division=0)

sil = silhouette_score(all_embeddings, all_trues)
db_index = davies_bouldin_score(all_embeddings, all_trues)
ch_score = calinski_harabasz_score(all_embeddings, all_trues)

# Results display 
print("\n" + "="*35)
print(f"MODEL: {model_path}")
print("="*35)
print(f"Accuracy:          {acc:.4f}")
print(f"Precision:         {prec:.4f}")
print(f"F1 Score:          {f1:.4f}")
print("-" * 35)
print(f"Silhouette Score:  {sil:.4f}  (↑)")
print(f"Davies-Bouldin:    {db_index:.4f}  (↓)")
print(f"Calinski-Harabasz: {ch_score:.4f} (↑)")
print("="*35)

# Data visualization
print("Generating t-SNE plot...")
tsne = TSNE(n_components=2, random_state=42, init='pca', learning_rate='auto')
embeddings_2d = tsne.fit_transform(all_embeddings)

plt.figure(figsize=(10, 7))
scatter = plt.scatter(embeddings_2d[:, 0], embeddings_2d[:, 1], c=all_trues, cmap='tab10', alpha=0.6)
plt.colorbar(scatter, label='Class Label')
plt.title(f"t-SNE Projection\nF1: {f1:.3f} | Sil: {sil:.3f} | DBI: {db_index:.3f}")
plt.grid(True, linestyle='--', alpha=0.5)
plt.show()

# Save embeddings 
with h5py.File(h5_name, 'w') as hf: 
    hf.create_dataset('embeddings', data=all_embeddings)
    hf.create_dataset('labels', data=all_trues)
print(f"Done! Embeddings saved to {h5_name}")
